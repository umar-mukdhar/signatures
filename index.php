                "alveal-default.jpg": ["images/home/works/alveal1.jpg", "images/home/works/alveal2.jpg", "images/home/works/alveal3.jpg", "images/home/works/alveal-default.jpg"]
            };

            const imgName = originalSrc.split('/').pop();

            work.addEventListener('mouseenter', () => {
                if (images[imgName]) {
                    interval = setInterval(() => {
                        img.style.opacity = 0; // Start fading out
                        setTimeout(() => {
                            img.src = images[imgName][index]; // Change image
                            img.style.opacity = 1; // Fade in new image
                            index = (index + 1) % images[imgName].length;
                        }, 300); // Delay image change to match fade-out effect
                    }, 600); // Change image every 0.9s
                }
            });

            work.addEventListener('mouseleave', () => {
                clearInterval(interval);
                img.style.opacity = 0;
                setTimeout(() => {
                    img.src = originalSrc; // Reset image
                    img.style.opacity = 1;
                }, 300);
                index = 0;
            });
        });
    </script>

