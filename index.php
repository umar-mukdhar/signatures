    <script>
        const textElement = document.getElementById('scrollText');
        const text = textElement.innerText;
        textElement.innerHTML = ''; // Clear original text

        // Split text into individual words
        text.split(' ').forEach((word, wordIndex) => {
            const wordSpan = document.createElement('span');
            wordSpan.classList.add('word');

            // Split each word into individual characters
            word.split('').forEach((char) => {
                const charSpan = document.createElement('span');
                charSpan.classList.add('char');
                charSpan.textContent = char;
                wordSpan.appendChild(charSpan);
            });

            textElement.appendChild(wordSpan);

            // Add a space after each word
            if (wordIndex < text.split(' ').length - 1) {
                textElement.appendChild(document.createTextNode('\u00A0'));
            }
        });

        const chars = document.querySelectorAll('.char');
        let lastScrollTop = 0;
        let currentActiveIndex = 0;
        let animationTriggered = false;
        let lastScrollTime = Date.now();
        const section = document.getElementById('textSection');

        // Intersection Observer to detect when section enters the viewport
        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                animationTriggered = entry.isIntersecting;
            });
        }, {
            threshold: 0.5
        });

        observer.observe(section);

        window.addEventListener('scroll', () => {

            if (!animationTriggered) return;

            let scrollTop = window.scrollY;
            let currentTime = Date.now();
            let scrollSpeed = Math.abs(scrollTop - lastScrollTop) / (currentTime - lastScrollTime);

            chars.forEach((char, index) => {
                char.style.transitionDelay = `${0.05 * scrollSpeed}s`; // Adjust delay based on speed
            });

            if (scrollTop > lastScrollTop) {
                if (currentActiveIndex < chars.length) {
                    chars[currentActiveIndex].classList.add('active');
                    currentActiveIndex++;
                }
            } else {
                if (currentActiveIndex > 0) {
                    currentActiveIndex--;
                    chars[currentActiveIndex].classList.remove('active');
                }
            }

            lastScrollTop = scrollTop;
            lastScrollTime = currentTime;

            chars.forEach((char, index) => {
                if (index >= currentActiveIndex) {
                    char.classList.remove('active');
                }
            });
        });
    </script>

