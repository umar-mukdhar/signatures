    <script>
        // Initialize Lenis with slower scroll speed settings
        const lenis = new Lenis({
            duration: 1, // Increase duration for slower scroll
            easing: (t) => 1 - Math.pow(1 - t, 3) // Optional: Custom easing for smoother, slower scroll
        });

        // Listen for the scroll event and log the event data
        lenis.on('scroll', (e) => {
            // console.log(e);
        });

        // Use requestAnimationFrame to continuously update the scroll
        function raf(time) {
            lenis.raf(time);
            requestAnimationFrame(raf);
        }

        requestAnimationFrame(raf);
    </script>

    <script>
        document.querySelectorAll(".banner-link-container a").forEach(link => {
            link.addEventListener("click", function(e) {
                e.preventDefault();

                const selectedCategory = this.textContent.toLowerCase(); // Get the clicked category
                const works = document.querySelectorAll(".works");

                works.forEach(work => {
                    const categories = work.getAttribute("data-category").split(" "); // Get all categories in an array

                    if (selectedCategory === "all" || categories.includes(selectedCategory)) {
                        work.style.display = "block"; // Show matching elements
                    } else {
                        work.style.display = "none"; // Hide others
                    }
                });

                // Scroll to works-flex div smoothly
                document.querySelector(".works-flex").scrollIntoView({
                    behavior: "smooth",
                    block: "start"
                });
            });
        });
    </script>



